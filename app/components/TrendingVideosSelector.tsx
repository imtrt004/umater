export default function TrendingVideosSelector({ onVideosLoaded }: TrendingVideosSelectorProps) {
  const [selectedCountry, setSelectedCountry] = useState('US');
  const [loading, setLoading] = useState(false);
  const [searchQuery, setSearchQuery] = useState('');
  const [buttonCountry, setButtonCountry] = useState('United States');
  
  // Filter countries based on search query
  const filteredCountries = searchQuery.trim() === '' 
    ? COUNTRIES 
    : COUNTRIES.filter(country => 
        country.name.toLowerCase().includes(searchQuery.toLowerCase()) || 
        country.code.toLowerCase().includes(searchQuery.toLowerCase())
      );
  
  // Effect to update button text when country changes
  useEffect(() => {
    const country = COUNTRIES.find(c => c.code === selectedCountry);
    if (country) {
      console.log("Country updated to:", country.name);
      setButtonCountry(country.name);
    }
  }, [selectedCountry]);
  
  // Effect to auto-select first country when search filters to a single country
  useEffect(() => {
    // If search narrows down to exactly one country, auto-select it
    if (filteredCountries.length === 1 && filteredCountries[0].code !== selectedCountry) {
      setSelectedCountry(filteredCountries[0].code);
    }
  }, [filteredCountries, selectedCountry]);
  
  const fetchTrendingVideos = async () => {
    if (!selectedCountry) return;
    
    setLoading(true);
    
    try {
      const response = await fetchTrendingVideosByCountry(selectedCountry);
      
      if (response.items && response.items.length > 0) {
        onVideosLoaded(response.items, selectedCountry);
      }
    } catch (error) {
      console.error('Error fetching trending videos:', error);
    } finally {
      setLoading(false);
    }
  };
  
  // Get the current country name based on the selected country code
  const currentCountry = COUNTRIES.find(country => country.code === selectedCountry);
  const countryName = currentCountry ? currentCountry.name : selectedCountry;
  
  return (
    <div className="bg-white dark:bg-gray-800 rounded-xl shadow-lg p-6 w-full max-w-4xl">
      <h2 className="text-xl font-bold mb-4 text-gray-900 dark:text-white">
        Trending Videos by Country
      </h2>
      
      <div className="flex flex-col sm:flex-row gap-4">
        <div className="flex-1">
          <div className="mb-2">
            <input
              type="text"
              placeholder="Search countries..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="w-full px-4 py-2 bg-white dark:bg-gray-700 border border-gray-300 dark:border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-blue-500 mb-2"
            />
          </div>
          
          <div className="relative">
            <select
              id="country-select"
              value={selectedCountry}
              onChange={(e) => {
                const newCountry = e.target.value;
                console.log("Selected new country:", newCountry);
                setSelectedCountry(newCountry);
              }}
              className="block w-full px-4 py-2 bg-white dark:bg-gray-700 border border-gray-300 dark:border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
              size={1}
            >
              {filteredCountries.map((country) => (
                <option key={country.code} value={country.code}>
                  {country.name} ({country.code})
                </option>
              ))}
            </select>
            
            {filteredCountries.length === 0 && (
              <div className="absolute inset-0 flex items-center justify-center bg-white/80 dark:bg-gray-700/80 rounded-md">
                <p className="text-sm text-gray-500 dark:text-gray-400">No countries found</p>
              </div>
            )}
          </div>
          
          <div className="text-sm text-gray-500 dark:text-gray-400 mt-2">
            {filteredCountries.length} countries available
          </div>
        </div>
        
        <div className="flex items-end">
          <button
            onClick={fetchTrendingVideos}
            disabled={loading}
            className="w-full sm:w-auto px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors flex items-center justify-center gap-2 disabled:bg-blue-400"
          >
            {loading ? (
              <>
                <FaSpinner className="animate-spin" />
                Loading...
              </>
            ) : (
              <>
                <FaGlobe />
                Get Trending in {buttonCountry}
              </>
            )}
          </button>
        </div>
      </div>
      
      <p className="text-sm text-gray-600 dark:text-gray-400 mt-2">
        Find what videos are trending in different countries to discover content opportunities.
      </p>
    </div>
  );
} 